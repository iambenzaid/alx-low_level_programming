#include "3-calc.h"

/**
* op_add - The result of sum two numbers
* @a: First number
* @b: Second number
*
* Return: The sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - The result of sub two numbers
* @a: First number
* @b: Second number
*
* Return: The difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - The result of mult two numbers
* @a: First number
* @b: Second number
*
* Return: The result
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - The result of dividing two numbers
* @a: First number
* @b: Second number
*
* Return: The result
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - the mod of dividing two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: The mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
